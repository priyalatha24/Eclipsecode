package day3;

public class Assignmentoperator {

	public static void main(String[] args) {
		// assignment opearator Increment &decremnet 
		//declare variables
		int a = 12, b =12;
		int result1, result2;
		
		//origonal value
		System.out.println("Value of a:"+a);
		
		//incre operator
		result1 = ++a;
		System.out.println("After incremnet:"+result1);
		
		System.out.println("Value of b:"+b);
		
		//decre operator 
		result2 =--b;
		System.out.println("After decrement:"+result2);
		
   	

	}

}
